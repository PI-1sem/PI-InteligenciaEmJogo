    private void voltarButtonActionPerformed(java.awt.event.ActionEvent evt) {
        var nivelAcesso= Usuario.usuarioLogado.getNivelAcesso();
        if (nivelAcesso == "admin"){
            new TelaInicioProfessor().setVisible(true);
            this.dispose();
        }
        if(nivelAcesso == "aluno"){
            new TelaInicioAluno().setVisible(true);
            this.dispose();
        } 
    }
