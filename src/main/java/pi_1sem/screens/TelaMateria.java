    private Integer retornaSaldo(){
        try {
            var usuarioDao= new UsuarioDAO();
            var usuarioPont= usuarioDao.pegarPontuacao();

            return usuarioPont;
        } 
        catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Erro ao retornar saldo");
            return null;
        }
    }
