    private void entrarButtonActionPerformed(java.awt.event.ActionEvent evt) {
        String email = emailTextField.getText();
        var senha = senhaPasswordField.getPassword();
        String senhaString= new String(senha);

        if(email.equals("E-mail:") || senhaString.equals("Senha:")) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos!");
            return;
        }

        try{
            var  usuario = new Usuario(email, senhaString);

            var login = new LoginDAO();

            var usuarioLogado= login.existe(usuario);

            if(usuarioLogado == null){
                JOptionPane.showMessageDialog(null, "Usuário ou senha inválidos");  
            }
            else{
                if(usuarioLogado.getNivelAcesso().equals("admin")){
                    new TelaInicioADM().setVisible(true);
                    dispose();
                }
                else if(usuarioLogado.getNivelAcesso().equals("aluno")){
                    new Jogar().setVisible(true);
                    dispose();
                }
            }
        }
        catch(Exception e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Erro ao logar, tente novamente mais tarde");
        }

        emailTextField.setForeground(Color.GRAY);
        emailTextField.setText("E-mail:");
        
        senhaPasswordField.setForeground(Color.GRAY);
        senhaPasswordField.setText("Senha:");
        senhaPasswordField.setEchoChar((char) 0);
    }

