    private void configurarPlaceholders() {
       
        emailTextField.setForeground(Color.GRAY);
        emailTextField.setText("E-mail:");
        emailTextField.addFocusListener(new FocusListener() {
            @Override
            public void focusGained(FocusEvent e) {
                if (emailTextField.getText().equals("E-mail:")) {
                    emailTextField.setText("");
                    emailTextField.setForeground(Color.BLACK);
                }
            }
            
            @Override
            public void focusLost(FocusEvent e) {
                if (emailTextField.getText().isEmpty()) {
                    emailTextField.setForeground(Color.GRAY);
                    emailTextField.setText("E-mail:");
                }
            }
        });

        // Configurar campo de senha
        senhaPasswordField.setForeground(Color.GRAY);
        senhaPasswordField.setText("Senha:");
        senhaPasswordField.setEchoChar((char) 0);
        senhaPasswordField.addFocusListener(new FocusListener() {
            @Override
            public void focusGained(FocusEvent e) {
                var password = new String(senhaPasswordField.getPassword());
                if (password.equals("Senha:")) {
                    senhaPasswordField.setText("");
                    senhaPasswordField.setForeground(Color.BLACK);
                    senhaPasswordField.setEchoChar('*');
                }
            }
            
            @Override
            public void focusLost(FocusEvent e) {
                if (senhaPasswordField.getPassword().length == 0) {
                    senhaPasswordField.setForeground(Color.GRAY);
                    senhaPasswordField.setText("Senha:");
                    senhaPasswordField.setEchoChar((char) 0);
                }
            }
        });
    }
