    private String retornaNome(){
        try {
            var usuarioDao= new UsuarioDAO();
            var usuarioNome= usuarioDao.pegarNome();

            return usuarioNome;
        } 
        catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Erro ao retornat nome de usuario");
            return null;
        }
    }
    private void listarAlunos() {
        try {
            var alunoDAO= new AlunoDAO();
            DefaultTableModel model = (DefaultTableModel) alunosTable.getModel();
            model.setNumRows(0);


            ArrayList<Aluno> alunos = alunoDAO.listar();

            for (Aluno i: alunos) {
                model.addRow(new Object[]{
                    i.getId(),
                    i.getNome(),
                    i.getEmail(),
                    i.getSenha()
                });
            }   
        } 
        
        catch (Exception e) {
           e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Erro ao listar alunos");
        }
    }
    private Integer pegarCampoId(){
        int linhaSelecionada = alunosTable.getSelectedRow();   
        int idSelecionado= Integer.parseInt(alunosTable.getModel().getValueAt(linhaSelecionada, 0).toString());

        return idSelecionado;
    }
    private void capturarAltercoes(){
        var model = (DefaultTableModel) alunosTable.getModel();
    
    // Adiciona um listener único (evita múltiplas instâncias)
    model.removeTableModelListener(this::handleTableUpdate);
    model.addTableModelListener(this::handleTableUpdate);
    }

    private void handleTableUpdate(TableModelEvent e) {
        if (e.getType() != TableModelEvent.UPDATE || e.getColumn() < 1) return;
        
        int row = e.getFirstRow();
        int col = e.getColumn();
        
        if (row < 0 || row >= alunosTable.getRowCount()) return;

        var valorAlterado = alunosTable.getModel().getValueAt(row, col).toString();
        var id = Integer.parseInt(alunosTable.getModel().getValueAt(row, 0).toString());

        if (col == 1) {
            try{
                AlunoDAO alunoDAO = new AlunoDAO();
                alunoDAO.atualizarNome(valorAlterado, id);

                JOptionPane.showMessageDialog(null, "O nome foi alterado com sucesso");
            }
            catch(Exception ex){
                ex.printStackTrace();
                JOptionPane.showMessageDialog(null, "Erro ao alterar o nome");
            }
        }
        if (col == 2) {
            if(!valorAlterado.contains("@")){
                JOptionPane.showMessageDialog(null, "ERRO! O email deve conter @");
                return;
            }
            try{
                AlunoDAO alunoDAO = new AlunoDAO();
                alunoDAO.atualizarEmail(valorAlterado, id);

                JOptionPane.showMessageDialog(null, "O email foi alterado com sucesso");
            }
            catch(Exception ex){
                ex.printStackTrace();
                JOptionPane.showMessageDialog(null, "Erro ao alterar o email");
            }
        }
        if (col == 3) {
            if(valorAlterado.length() < 8){
                JOptionPane.showMessageDialog(null, "ERRO! A senha deve conter no mínimo 8 caracteres");
                return;
            }
            try{
                AlunoDAO alunoDAO = new AlunoDAO();
                alunoDAO.atualizarSenha(valorAlterado, id);
                JOptionPane.showMessageDialog(null, "A senha foi alterada com sucesso");
            }
            catch(Exception ex){
                ex.printStackTrace();
                JOptionPane.showMessageDialog(null, "Erro ao alterar a senha");
            }
        }
    }
