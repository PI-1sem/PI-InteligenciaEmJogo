    private void adcionarAlunoButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adcionarAlunoButtonActionPerformed
        // TODO add your handling code here:
        String Nome = nomeAlunoTextField.getText();       
        String Email = emailAlunoTextField.getText();
        String Senha = senhaAlunoTextField.getText();
        DefaultTableModel tabelaAluno = 
                        (DefaultTableModel)tabelaAlunoTable.getModel();
        
        Object[] novoAluno = new Object[]{
            Nome,  //primeiro            
            Email,  // Segundo
            Senha, // Terceiro
        };
        
        tabelaAluno.addRow(novoAluno);
        //limparCampos
        nomeAlunoTextField.setText("");                
        emailAlunoTextField.setText("");
        senhaAlunoTextField.setText("");
        
        
        
    }//GEN-LAST:event_adcionarAlunoButtonActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = tabelaAlunoTable.getSelectedRow();
        if (linhaSelecionada == 1){
        JOptionPane.showMessageDialog(this, "Por favor, selecione uma linha!");
    }
        else{
               DefaultTableModel tabelaProfessor = 
                        (DefaultTableModel)tabelaAlunoTable.getModel(); 
               tabelaProfessor.removeRow(linhaSelecionada);
               JOptionPane.showMessageDialog(this, "Professor excluido!");
                }
    }   
        
